/* Write a set of VGA registers to switch modes */
static void vga_write_registers(uint8_t misc, const uint8_t* seq,
                                const uint8_t* crtc, const uint8_t* gc,
                                const uint8_t* ac) {
    /* Miscellaneous register */
    outb(0x3C2, misc);

    /* Sequencer registers */
    for (int i = 0; i < 5; i++) {
        outb(0x3C4, i);
        outb(0x3C5, seq[i]);
    }

    /* Unlock CRTC registers (clear bit 7 of register 0x11) */
    outb(0x3D4, 0x03);
    outb(0x3D5, inb(0x3D5) | 0x80);
    outb(0x3D4, 0x11);
    outb(0x3D5, inb(0x3D5) & ~0x80);

    /* CRTC registers */
    for (int i = 0; i < 25; i++) {
        outb(0x3D4, i);
        outb(0x3D5, crtc[i]);
    }

    /* Graphics controller registers */
    for (int i = 0; i < 9; i++) {
        outb(0x3CE, i);
        outb(0x3CF, gc[i]);
    }

    /* Attribute controller registers */
    for (int i = 0; i < 21; i++) {
        inb(0x3DA);        /* Reset AC flip-flop */
        outb(0x3C0, i);
        outb(0x3C0, ac[i]);
    }

    /* Lock palette, enable display */
    inb(0x3DA);
    outb(0x3C0, 0x20);
}

/* Set up the default 256-color palette */
static void vga_set_default_palette(void) {
    /* First 16: standard CGA/EGA colors */
    static const uint8_t basic_colors[][3] = {
        { 0,  0,  0},  /* 0: black */
        { 0,  0, 42},  /* 1: dark blue */
        { 0, 42,  0},  /* 2: dark green */
        { 0, 42, 42},  /* 3: dark cyan */
        {42,  0,  0},  /* 4: dark red */
        {42,  0, 42},  /* 5: dark magenta */
        {42, 21,  0},  /* 6: brown */
        {42, 42, 42},  /* 7: light gray */
        {21, 21, 21},  /* 8: dark gray */
        {21, 21, 63},  /* 9: blue */
        {21, 63, 21},  /* 10: green */
        {21, 63, 63},  /* 11: cyan */
        {63, 21, 21},  /* 12: red */
        {63, 21, 63},  /* 13: magenta */
        {63, 63, 21},  /* 14: yellow */
        {63, 63, 63},  /* 15: white */
    };

    for (int i = 0; i < 16; i++) {
        vga_set_palette_color(i, basic_colors[i][0], basic_colors[i][1], basic_colors[i][2]);
    }

    /* 16-31: grayscale ramp */
    for (int i = 0; i < 16; i++) {
        uint8_t v = i * 4;
        vga_set_palette_color(16 + i, v, v, v);
    }

    /* 32-255: color ramp (rainbow) */
    for (int i = 0; i < 224; i++) {
        uint8_t r, g, b;
        int section = i / 32;
        int offset = (i % 32) * 2;
        switch (section) {
            case 0: r = 63;      g = offset;  b = 0;       break; /* red to yellow */
            case 1: r = 63-offset; g = 63;    b = 0;       break; /* yellow to green */
            case 2: r = 0;       g = 63;      b = offset;  break; /* green to cyan */
            case 3: r = 0;       g = 63-offset; b = 63;    break; /* cyan to blue */
            case 4: r = offset;  g = 0;       b = 63;      break; /* blue to magenta */
            case 5: r = 63;      g = 0;       b = 63-offset; break; /* magenta to red */
            default: r = offset; g = offset;  b = offset;  break; /* grayscale fill */
        }
        vga_set_palette_color(32 + i, r, g, b);
    }
}

void vga_init(void) {
    vga_write_registers(mode_13h_misc, mode_13h_seq, mode_13h_crtc,
                        mode_13h_gc, mode_13h_ac);
    vga_set_default_palette();
    vga_clear(0);
}

void vga_exit(void) {
    vga_write_registers(mode_text_misc, mode_text_seq, mode_text_crtc,
                        mode_text_gc, mode_text_ac);
}

/* === Drawing Primitives === */

void vga_clear(uint8_t color) {
    for (int i = 0; i < VGA_WIDTH * VGA_HEIGHT; i++) {
        back_buffer[i] = color;
    }
    vga_swap();
}

void vga_putpixel(int x, int y, uint8_t color) {
    if (x >= 0 && x < VGA_WIDTH && y >= 0 && y < VGA_HEIGHT) {
        back_buffer[y * VGA_WIDTH + x] = color;
    }
}

uint8_t vga_getpixel(int x, int y) {
    if (x >= 0 && x < VGA_WIDTH && y >= 0 && y < VGA_HEIGHT) {
        return back_buffer[y * VGA_WIDTH + x];
    }
    return 0;
}

void vga_swap(void) {
    /* Copy back buffer to VGA framebuffer */
    uint8_t* src = back_buffer;
    uint8_t* dst = VGA_FRAMEBUFFER;
    for (int i = 0; i < VGA_WIDTH * VGA_HEIGHT; i++) {
        dst[i] = src[i];
    }
}

void vga_line(int x0, int y0, int x1, int y1, uint8_t color) {
    /* Bresenham's line algorithm */
    int dx = x1 - x0;
    int dy = y1 - y0;
    int sx = dx > 0 ? 1 : -1;
    int sy = dy > 0 ? 1 : -1;
    dx = dx < 0 ? -dx : dx;
    dy = dy < 0 ? -dy : dy;

    int err = dx - dy;

    while (1) {
        vga_putpixel(x0, y0, color);
        if (x0 == x1 && y0 == y1) break;
        int e2 = 2 * err;
        if (e2 > -dy) { err -= dy; x0 += sx; }
        if (e2 <  dx) { err += dx; y0 += sy; }
    }
}

void vga_rect(int x, int y, int w, int h, uint8_t color) {
    vga_line(x, y, x + w - 1, y, color);         /* top */
    vga_line(x, y + h - 1, x + w - 1, y + h - 1, color); /* bottom */
    vga_line(x, y, x, y + h - 1, color);         /* left */
    vga_line(x + w - 1, y, x + w - 1, y + h - 1, color); /* right */
}

void vga_fillrect(int x, int y, int w, int h, uint8_t color) {
    for (int py = y; py < y + h; py++) {
        for (int px = x; px < x + w; px++) {
            vga_putpixel(px, py, color);
        }
    }
}

void vga_circle(int cx, int cy, int r, uint8_t color) {
    /* Midpoint circle algorithm */
    int x = r, y = 0;
    int err = 1 - r;

    while (x >= y) {
        vga_putpixel(cx + x, cy + y, color);
        vga_putpixel(cx - x, cy + y, color);
        vga_putpixel(cx + x, cy - y, color);
        vga_putpixel(cx - x, cy - y, color);
        vga_putpixel(cx + y, cy + x, color);
        vga_putpixel(cx - y, cy + x, color);
        vga_putpixel(cx + y, cy - x, color);
        vga_putpixel(cx - y, cy - x, color);
        y++;
        if (err < 0) {
            err += 2 * y + 1;
        } else {
            x--;
            err += 2 * (y - x) + 1;
        }
    }
}

void vga_fillcircle(int cx, int cy, int r, uint8_t color) {
    for (int y = -r; y <= r; y++) {
        for (int x = -r; x <= r; x++) {
            if (x * x + y * y <= r * r) {
                vga_putpixel(cx + x, cy + y, color);
            }
        }
    }
}

void vga_set_palette_color(uint8_t index, uint8_t r, uint8_t g, uint8_t b) {
    outb(0x3C8, index);
    outb(0x3C9, r & 0x3F);
    outb(0x3C9, g & 0x3F);
    outb(0x3C9, b & 0x3F);
}

